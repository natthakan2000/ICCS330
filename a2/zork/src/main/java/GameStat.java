public class GameStat {
    private String name;
    private int hp;
    private int power;
    public void setName(String name){
        this.name = name;
    }
    public String getName(){
        return name;
    }
    public void setHp(int hp){
        this.hp = hp;
    }
    public int getHp(){
        return hp;
    }
    public void setPower(int power){
        this.power = power;
    }
    public int getPower(){
        return power;
    }
}
